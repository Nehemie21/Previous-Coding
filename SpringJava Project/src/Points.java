public class Points {
   
    private float pointx1;
    private float pointy1;
 
   
    public Points(float px1, float py1)
    {
        pointx1 = px1;
        pointy1 = py1;
       
    }
   
   
    public Points()
    {
       
    }

    public float getPointx1() {
        return pointx1;
    }

    public void setPointx1(float px) {
        pointx1 = px;
    }

    public float getPointy1() {
        return pointy1;
    }

    public void setPointy1(float py) {
        pointy1 = py;
    }

   
   
   
   
}
